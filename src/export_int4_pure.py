def quantize_weight_nf4(weight_np: np.ndarray) -> tuple:
    """将一个 2D 权重矩阵量化为 NF4 格式.

    Args:
        weight_np: numpy array, shape (out_features, in_features), dtype float32

    Returns:
        packed_bytes: bytes, 每 2 个 4-bit 值打包为 1 个 uint8
        scale: float, 缩放因子
        zero_point: float, 零点 (NF4 是对称的，zero_point 通常为 0)
        original_shape: tuple, 原始 shape
    """
    # 计算 absmax
    absmax = np.abs(weight_np).max()
    if absmax == 0:
        # 全零权重
        n_elements = weight_np.size
        n_packed = (n_elements + 1) // 2
        return bytes(n_packed), 1.0, 0.0, weight_np.shape

    # scale: 将 absmax 映射到 NF4 的最大值 (1.0)
    scale = absmax  # 因为 NF4 表范围是 [-1, 1]

    # 归一化到 [-1, 1]
    normalized = weight_np / scale  # 范围 [-1, 1]

    # 量化到 NF4 表
    # 对每个元素，找到 NF4_TABLE 中最近的值
    flat = normalized.flatten()
    # 使用向量化查找
    indices = np.argmin(np.abs(flat[:, None] - NF4_TABLE[None, :]), axis=1)
    quantized = NF4_TABLE[indices]

    # 将 4-bit 索引打包为字节
    # 每 2 个索引 (0-15) 打包为 1 个 uint8
    indices_uint8 = indices.astype(np.uint8)
    n_elements = len(indices_uint8)
    n_packed = (n_elements + 1) // 2

    packed = bytearray(n_packed)
    for i in range(n_packed):
        high = indices_uint8[2 * i] & 0x0F
        low = indices_uint8[2 * i + 1] & 0x0F if 2 * i + 1 < n_elements else 0
        packed[i] = (high << 4) | low

    return bytes(packed), float(scale), 0.0, weight_np.shape


def export_int4_pure(model_dir: str, output_dir: str) -> str:
    """使用纯 Python NF4 量化导出 INT4 模型."""
    from safetensors import safe_open
    import safetensors.torch as st_torch
    import torch

    model_path_obj = Path(model_dir)
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    logger.info("=" * 60)
    logger.info("INT4 量化导出 (纯 Python NF4 实现)")
    logger.info("  输入: %s", model_dir)
    logger.info("  输出: %s", output_dir)
    logger.info("=" * 60)

    # ── 1. 读取模型索引 ──────────────────────────────────────────────
    index_path = model_path_obj / "model.safetensors.index.json"
    with open(index_path) as f:
        index = json.load(f)
    weight_map = index.get("weight_map", {})

    # ── 2. 筛选 action head 的 key ───────────────────────────────────
    backbone_keywords = ['backbone', 'qwen', 'model.model.backbone', 'model.model.model']
    action_head_keys = [
        k for k in weight_map
        if not any(kw in k for kw in backbone_keywords)
    ]
    logger.info("总权重数: %d", len(weight_map))
    logger.info("Action head 权重数: %d", len(action_head_keys))

    # 按文件分组
    file_to_keys = {}
    for key in action_head_keys:
        sf_file = weight_map[key]
        file_to_keys.setdefault(sf_file, []).append(key)

    # ── 3. 保存配置文件 ──────────────────────────────────────────────
    config_src = model_path_obj / "config.json"
    if config_src.exists():
        with open(config_src) as f:
            config = json.load(f)
        config["quantization_config"] = {
            "quant_method": "bitsandbytes",
            "load_in_4bit": True,
            "bnb_4bit_compute_dtype": "float16",
            "bnb_4bit_quant_type": "nf4",
            "bnb_4bit_use_double_quant": True,
        }
        with open(output_path / "config.json", "w") as f:
            json.dump(config, f, indent=2)
        logger.info("✅ config.json 已保存")

    for fname in ["processor_config.json", "statistics.json", "embodiment_id.json"]:
        src = model_path_obj / fname
        if src.exists():
            shutil.copy2(src, output_path / fname)
            logger.info("✅ %s 已复制", fname)

    # ── 4. 逐文件量化 ───────────────────────────────────────────────
    total_params = 0
    quantized_params = 0
    start_time = time.time()

    # 用于存储量化后的权重 (PyTorch 格式)
    all_quantized = {}

    for sf_idx, (sf_name, keys) in enumerate(sorted(file_to_keys.items())):
        sf_path = model_path_obj / sf_name
        logger.info("[%d/%d] 处理: %s (%d keys)",
                     sf_idx + 1, len(file_to_keys), sf_name, len(keys))

        with safe_open(sf_path, framework="pt", device="cpu") as f:
            all_keys_in_file = list(f.keys())
            logger.info("  文件总张量: %d", len(all_keys_in_file))

            for key in keys:
                if key not in all_keys_in_file:
                    continue

                tensor = f.get_tensor(key)
                total_params += tensor.numel()

                # 只量化 2D 权重
                if tensor.ndim == 2 and tensor.shape[0] > 1 and tensor.shape[1] > 1:
                    # 转为 numpy
                    weight_np = tensor.float().numpy()

                    # NF4 量化
                    packed_bytes, scale, zero_point, orig_shape = quantize_weight_nf4(weight_np)

                    # 保存为 numpy 数组 (可以后续转为 torch 保存)
                    all_quantized[key] = {
                        'data': packed_bytes,
                        'scale': scale,
                        'zero_point': zero_point,
                        'shape': orig_shape,
                        'dtype': 'nf4',
                    }
                    quantized_params += tensor.numel()
                else:
                    # 非 2D 张量直接保存
                    all_quantized[key] = {
                        'data': tensor,
                        'dtype': 'raw',
                    }

        # 保存量化后的张量
        # 使用自定义格式保存
        out_sf_path = output_path / sf_name
        _save_quantized_tensors(all_quantized, str(out_sf_path))
        logger.info("  → 已保存: %s", out_sf_path)

        # 释放内存
        all_quantized.clear()

    elapsed = time.time() - start_time

    # ── 5. 统计 ──────────────────────────────────────────────────────
    size_mb = sum(
        f.stat().st_size for f in output_path.rglob("*") if f.is_file()
    ) / (1024 ** 2)
    file_count = sum(1 for _ in output_path.rglob("*") if _.is_file())

    logger.info("=" * 60)
    logger.info("✅ INT4 量化导出完成!")
    logger.info("  路径: %s", output_path)
    logger.info("  大小: %.0f MB (%d files)", size_mb, file_count)
    logger.info("  总参数: %d", total_params)
    logger.info("  量化参数: %d (%.1f%%)", quantized_params, 100 * quantized_params / max(total_params, 1))
    logger.info("  耗时: %.1f 秒", elapsed)
    logger.info("=" * 60)

    return str(output_path)


def _save_quantized_tensors(tensors: dict, output_path: str):
    """保存量化张量为文件.

    对于 NF4 量化的张量，保存为 {key}.nf4 文件。
    对于原始张量，保存为 safetensors 格式。

    为了简化，我们使用 PyTorch 的 torch.save 保存所有数据。
    """
    import torch

    # 分离原始张量和量化张量
    raw_tensors = {}
    quantized_tensors = {}

    for key, value in tensors.items():
        if value['dtype'] == 'raw':
            raw_tensors[key] = value['data']
        else:
            # 将量化数据转为 torch 张量
            shape = value['shape']
            n_elements = shape[0] * shape[1]
            n_packed = (n_elements + 1) // 2

            # 保存 packed bytes + scale + shape
            packed_tensor = torch.frombuffer(bytearray(value['data']), dtype=torch.uint8)
            scale_tensor = torch.tensor([value['scale']], dtype=torch.float32)

            quantized_tensors[key + '.packed'] = packed_tensor
            quantized_tensors[key + '.scale'] = scale_tensor
            quantized_tensors[key + '.shape'] = torch.tensor(shape, dtype=torch.int64)

    # 合并并保存
    all_tensors = {**raw_tensors, **quantized_tensors}
    torch.save(all_tensors, output_path)


